char	*ft_strchr(const char *s, int c)
{
	while (*s != c && *s != '\0')
	{
		if (*s == ((char)c))
			return ((char *)&*s);
		s++;
	}
	if (*s != c)
		return (0);
	return ((char *)s);
}
